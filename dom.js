const turn = () => {
    document.getElementById('wrong').innerHTML = ''
    let guess = document.getElementById("user-input").value.trim().toLowerCase()
    if (/^[a-zA-Z]/.test(guess) == true && guess.length == 1) {
        for (let i = 0; i < selectedWord.length; i++) {
            if (guess == selectedWord.charAt(i)) {
                hiddenWord = setCharAt(hiddenWord, i, guess)
            }
        }
        if (hiddenWord == selectedWord) {
            return document.getElementById("win").innerHTML = "You won!", document.getElementById("answer").innerHTML = selectedWord
        } else {
            return document.getElementById("guesses").innerHTML += guess, document.getElementById("answer").innerHTML = hiddenWord
        }
    } else {
        return document.getElementById("wrong").innerHTML = "Please enter a letter"
    }
}